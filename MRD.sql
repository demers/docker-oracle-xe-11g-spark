/* **********************************************************
	Sequence "SEQ_ID_VILLAGE"
********************************************************** */
CREATE SEQUENCE SEQ_ID_VILLAGE
	INCREMENT BY 1
	START WITH 1;

/* **********************************************************
	Table "LOGEMENT"
